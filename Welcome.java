public class Welcome {
    public static void main(String[] args) {
        System.out.println("Welcome to COSC 211!");
        System.out.print("My Name is:");
        System.out.println("Ari");
        // Escape Sequence
        // line break
        System.out.println("Line\nBreak");

        // tab space
        System.out.println("Tab\tSpace");

        // Literal backslash

        System.out.println("Literal\\Backslash");

        // Literal Quotes

        System.out.println("\"Quotes\"");

        // Formatted Print

        System.out.printf("Hello my name is: %s %s%n", "Ari", "Danon");
    }
}
